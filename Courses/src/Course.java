
public class Course {
	
	String title;
	int credit;
	String teacher;
	String department;
	String building;
	
	
	
	
	public void printTitle(){
		
		System.out.println("The name of the book is: "+ title);
	}
	
	public void printTeacher(){
		
		System.out.println("The edition of the book is: "+ teacher);
	}
	
	public void printCredit(){
		
		System.out.println("The number of pages is: "+ credit);
	}
	
	
	public void printDepartment(){
		
		System.out.println("The author of the book is: "+ department);
	}
	
	public void printBuilding(){
		
		System.out.println("The type of the book is: "+ building);
	}
	

}
